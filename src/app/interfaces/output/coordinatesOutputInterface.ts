/**
 * This interface represents coordinates.
 * Output entries.
 */
export interface CoordinatesOutputInterface {
  /**
   * This field is the coordinates' id.
   */
  id: number;

  /**
   * This field is the coordinates' latitude.
   */
  latitude: number;

  /**
   * This field is the coordinates' longitude.
   */
  longitude: number;
}
